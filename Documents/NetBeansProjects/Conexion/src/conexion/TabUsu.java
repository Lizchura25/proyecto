public void CargarDatos(){
        DefaultTableModel model=new DefaultTableModel();
        model.addColumn("CODIGO");
        model.addColumn("USER");
        model.addColumn("CONTRASEÃ‘A");
        model.addColumn("PRIBILEGIO");
        
        ResultSet rs=conn.ObtenerDatos("SELECT CodEmp,UseUsu,ConUsu,PriUsu FROM usuario");
        try{
            while(rs.next()){
                model.addRow(new Object[]{
                rs.getString("CodEmp"),
                rs.getString("UseUsu"),
                rs.getString("ConUsu"),
                rs.getString("PriUsu"),
            });
            }
             table_Cli.setModel(model);
        }catch(SQLException ex){
           ex.printStackTrace();
        }
    }
